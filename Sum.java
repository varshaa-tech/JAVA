public class Sum {
    public static void main(String[] args) {
        int a=2,b=3;
        System.out.println("sum :"+(a+b));
        System.out.println("subtraction:"+(a-b));
        System.out.println("division :"+(a/b));
        System.out.println(a+=b);
        System.out.println(a-=b);
        System.out.println(a*=b);
        System.out.println(a/=b);
    }
}
